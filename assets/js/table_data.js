// Function to create a table
function createTable(containerId, data) {
    const tableContainer = document.getElementById(containerId);
    const table = document.createElement('table');
    table.classList.add('rbt-table', 'table', 'table-borderless');

    // Create the table header
    const thead = document.createElement('thead');
    const headerRow = document.createElement('tr');
    const headers = ['', 'Standard bathtub available (1 room per person)', 'Economy Plus shower only (single rooms only)', 'Economy shower only (single room only)'];
    headers.forEach(headerText => {
        const th = document.createElement('th');
        th.textContent = headerText;
        headerRow.appendChild(th);
    });
    thead.appendChild(headerRow);
    table.appendChild(thead);

    // Create the table body
    const tbody = document.createElement('tbody');
    data.forEach(row => {
        const tr = document.createElement('tr');

        const weekCell = document.createElement('th');
        weekCell.textContent = row.weeks;
        tr.appendChild(weekCell);

        const standardCell = document.createElement('td');
        standardCell.textContent = row.standard.toLocaleString();
        tr.appendChild(standardCell);

        const economyPlusCell = document.createElement('td');
        economyPlusCell.textContent = row.economyPlus.toLocaleString();
        tr.appendChild(economyPlusCell);

        const economyCell = document.createElement('td');
        economyCell.textContent = row.economy.toLocaleString();
        tr.appendChild(economyCell);

        tbody.appendChild(tr);
    });
    table.appendChild(tbody);

    tableContainer.appendChild(table);
}

